"""
Sutta-specific search and study plan tools.
Queries the 'buddhist_suttas' Qdrant collection.
Used by both the FastAPI routes and the MCP server.
"""

from __future__ import annotations

import json
from functools import lru_cache
from pathlib import Path

from qdrant_client import QdrantClient
from qdrant_client.models import Filter, FieldCondition, MatchValue, MatchAny, Range

from search.ingestion.qdrant_setup import QDRANT_URL
from search.ingestion.embedder import get_embedder

SUTTA_COLLECTION = "buddhist_suttas"
REPO_ROOT = Path(__file__).parent.parent.parent
STUDY_PLANS_FILE = REPO_ROOT / "search" / "data" / "study_plans.json"
TEXTS_DIR = REPO_ROOT / "frontend" / "public" / "content" / "texts"


def is_local(sutta_id: str) -> bool:
    """Return True if a local text file exists for this sutta."""
    return (TEXTS_DIR / f"{sutta_id}.json").exists()

_sutta_client: QdrantClient | None = None


def get_sutta_client() -> QdrantClient:
    global _sutta_client
    if _sutta_client is None:
        _sutta_client = QdrantClient(url=QDRANT_URL)
    return _sutta_client


# ---------------------------------------------------------------------------
# Study Plans (loaded from JSON file generated by generate_study_plans.py)
# ---------------------------------------------------------------------------

@lru_cache(maxsize=1)
def _load_study_plans() -> list[dict]:
    """Load and cache study plans from JSON. Call invalidate_plans_cache() to reload."""
    if not STUDY_PLANS_FILE.exists():
        return []
    return json.loads(STUDY_PLANS_FILE.read_text())


def invalidate_plans_cache() -> None:
    """Force reload of study plans (call after regeneration)."""
    _load_study_plans.cache_clear()


def list_study_plans(
    theme_category: str | None = None,
    nikaya: str | None = None,
    min_size: int = 5,
    limit: int = 50,
) -> list[dict]:
    """
    List all available study plans, optionally filtered.

    Args:
        theme_category: Filter by theme (practice, doctrine, ethics, cosmology,
                        community, psychology, narrative, poetry, mixed)
        nikaya: Filter plans containing this nikaya (an, sn, mn, dn, ...)
        min_size: Minimum number of suttas in a plan
        limit: Maximum number of plans to return

    Returns:
        List of plan summaries (without the full sutta list for brevity)
    """
    plans = _load_study_plans()
    results = []

    for plan in plans:
        if plan["size"] < min_size:
            continue
        if theme_category and plan.get("theme_category") != theme_category:
            continue
        if nikaya and nikaya not in plan.get("nikaya_breakdown", {}):
            continue

        # Return summary (without suttas list — use get_study_plan for details)
        results.append({
            "cluster_id": plan["cluster_id"],
            "name": plan["name"],
            "description": plan["description"],
            "keywords": plan["keywords"],
            "theme_category": plan["theme_category"],
            "nikaya_breakdown": plan["nikaya_breakdown"],
            "size": plan["size"],
            "reading_minutes": plan["reading_minutes"],
        })

        if len(results) >= limit:
            break

    return results


def get_study_plan(cluster_id: int) -> dict | None:
    """
    Get the full study plan for a cluster, including the ordered sutta sequence.

    Args:
        cluster_id: Integer cluster ID (from /study-plans list)

    Returns:
        Full plan with ordered suttas list (each sutta includes local:bool), or None if not found
    """
    plans = _load_study_plans()
    for plan in plans:
        if plan["cluster_id"] == cluster_id:
            # Enrich each sutta with local availability
            enriched = dict(plan)
            enriched["suttas"] = [
                {**s, "local": is_local(s.get("sutta_id", ""))}
                for s in plan.get("suttas", [])
            ]
            return enriched
    return None


def find_study_plan_by_topic(topic: str, limit: int = 5) -> list[dict]:
    """
    Find study plans semantically matching a topic using Qdrant vector search.

    Searches for suttas matching the topic, then returns the study plans
    of the clusters they belong to.

    Args:
        topic: Natural language topic (e.g. "mindfulness", "dependent origination")
        limit: Number of plans to return

    Returns:
        List of matching study plans (summaries), ranked by relevance
    """
    embedder = get_embedder()
    client = get_sutta_client()

    query_vector = embedder.encode_query(topic)

    # Search for matching suttas
    results = client.query_points(
        collection_name=SUTTA_COLLECTION,
        query=query_vector,
        limit=30,
        with_payload=True,
    ).points

    # Collect clusters represented in results, ranked by hit count + score
    from collections import defaultdict
    cluster_scores: dict[int, float] = defaultdict(float)
    for hit in results:
        cid = hit.payload.get("cluster_id")
        if cid is not None:
            cluster_scores[int(cid)] += hit.score

    top_clusters = sorted(cluster_scores.items(), key=lambda x: -x[1])[:limit]

    # Load matching plans
    plans = _load_study_plans()
    plan_by_id = {p["cluster_id"]: p for p in plans}

    matched = []
    for cluster_id, _score in top_clusters:
        if cluster_id in plan_by_id:
            plan = plan_by_id[cluster_id]
            matched.append({
                "cluster_id": plan["cluster_id"],
                "name": plan["name"],
                "description": plan["description"],
                "keywords": plan["keywords"],
                "theme_category": plan["theme_category"],
                "nikaya_breakdown": plan["nikaya_breakdown"],
                "size": plan["size"],
                "reading_minutes": plan["reading_minutes"],
                "relevance_score": round(_score, 3),
            })

    return matched


