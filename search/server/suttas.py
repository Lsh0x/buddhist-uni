def search_suttas(
    query: str,
    nikaya: str | None = None,
    cluster_id: int | None = None,
    min_words: int | None = None,
    max_words: int | None = None,
    limit: int = 10,
) -> list[dict]:
    """
    Semantic search across 4167+ Sujato English sutta translations.

    Args:
        query: Natural language query (e.g. "breath meditation", "what is anattā?")
        nikaya: Filter by collection: an, sn, mn, dn, dhp, ud, iti, snp, thag, thig, ja
        cluster_id: Filter to a specific thematic cluster
        min_words / max_words: Filter by sutta length in words
        limit: Number of results (max 20)

    Returns:
        List of matching suttas with score, sutta_id, title, blurb, url, cluster
    """
    embedder = get_embedder()
    client = get_sutta_client()

    query_vector = embedder.encode_query(query)

    # Build filter
    conditions = []
    if nikaya:
        conditions.append(FieldCondition(key="nikaya", match=MatchValue(value=nikaya)))
    if cluster_id is not None:
        conditions.append(FieldCondition(key="cluster_id", match=MatchValue(value=cluster_id)))
    if min_words is not None or max_words is not None:
        conditions.append(FieldCondition(
            key="word_count",
            range=Range(gte=min_words, lte=max_words),
        ))

    qdrant_filter = Filter(must=conditions) if conditions else None

    results = client.query_points(
        collection_name=SUTTA_COLLECTION,
        query=query_vector,
        query_filter=qdrant_filter,
        limit=min(limit, 20),
        with_payload=True,
    ).points

    return [
        {
            "score": round(hit.score, 4),
            "sutta_id": hit.payload.get("sutta_id"),
            "nikaya": hit.payload.get("nikaya"),
            "nikaya_name": hit.payload.get("nikaya_name"),
            "title": hit.payload.get("title"),
            "blurb": hit.payload.get("blurb") or "",
            "word_count": hit.payload.get("word_count"),
            "difficulty": hit.payload.get("difficulty"),
            "cluster_id": hit.payload.get("cluster_id"),
            "cluster_name": hit.payload.get("cluster_name"),
            "url": hit.payload.get("url"),
            "local": is_local(hit.payload.get("sutta_id", "")),
        }
        for hit in results
    ]


def get_sutta(sutta_id: str) -> dict | None:
    """
    Get a specific sutta by its ID (e.g. 'dn2', 'mn36', 'sn12.15').

    Returns full payload including blurb, cluster info, and URL.
    """
    import hashlib
    point_id = int(hashlib.md5(sutta_id.encode()).hexdigest()[:16], 16) % (2**63)
    client = get_sutta_client()

    results = client.retrieve(
        collection_name=SUTTA_COLLECTION,
        ids=[point_id],
        with_payload=True,
    )
    if not results:
        return None

    p = results[0].payload
    sid = p.get("sutta_id", "")
    return {
        "sutta_id": sid,
        "nikaya": p.get("nikaya"),
        "nikaya_name": p.get("nikaya_name"),
        "title": p.get("title"),
        "blurb": p.get("blurb") or "",
        "word_count": p.get("word_count"),
        "segment_count": p.get("segment_count"),
        "difficulty": p.get("difficulty"),
        "translator": p.get("translator"),
        "cluster_id": p.get("cluster_id"),
        "cluster_name": p.get("cluster_name"),
        "url": p.get("url"),
        "local": is_local(sid),
    }


def get_similar_suttas(sutta_id: str, limit: int = 8) -> list[dict]:
    """
    Find suttas most similar to a given sutta (by vector proximity).

    Args:
        sutta_id: Reference sutta (e.g. 'mn10', 'sn47.1')
        limit: Number of similar suttas to return

    Returns:
        List of similar suttas ordered by similarity score
    """
    import hashlib
    point_id = int(hashlib.md5(sutta_id.encode()).hexdigest()[:16], 16) % (2**63)
    client = get_sutta_client()

    # Use Qdrant's recommend API (vector of the sutta itself as query)
    results = client.query_points(
        collection_name=SUTTA_COLLECTION,
        query=point_id,  # use existing vector by ID
        limit=limit + 1,  # +1 to exclude self
        with_payload=True,
    ).points

    return [
        {
            "score": round(hit.score, 4),
            "sutta_id": hit.payload.get("sutta_id"),
            "nikaya": hit.payload.get("nikaya"),
            "nikaya_name": hit.payload.get("nikaya_name", ""),
            "title": hit.payload.get("title"),
            "blurb": (hit.payload.get("blurb") or "")[:120],
            "word_count": hit.payload.get("word_count"),
            "difficulty": hit.payload.get("difficulty"),
            "cluster_id": hit.payload.get("cluster_id"),
            "cluster_name": hit.payload.get("cluster_name"),
            "url": hit.payload.get("url"),
            "local": is_local(hit.payload.get("sutta_id", "")),
        }
        for hit in results
        if hit.payload.get("sutta_id") != sutta_id  # exclude self
    ][:limit]
